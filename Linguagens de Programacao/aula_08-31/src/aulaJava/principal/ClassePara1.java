package aulaJava.principal;

public class ClassePara1 {

	public static void main(String[] args) {
		
		//CLASSE FOR = PARA

		// numeros crescentes
		for(int numero =0; numero<=10;numero++) {
			System.out.println("numero atual é: "+ numero);
		}
		
		System.out.println("------------------");
		
		// numeros decrescentes
		
		for (int num=10; num>=0; num--) {
			System.out.println("numero atual é: "+ num);
		}
		
	}

}
